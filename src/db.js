const { Pool } = require('pg');
const bcrypt = require('bcryptjs');

const ADMIN_USERNAME = 'rskuryer';
const ADMIN_PASSWORD = 'uncharted0833';
const ADMIN_ROLE = 'admin';
const BCRYPT_SALT_ROUNDS = 10;

function getDatabaseUrl() {
  const value = typeof process.env.DATABASE_URL === 'string'
    ? process.env.DATABASE_URL.trim().replace(/^['\"]|['\"]$/g, '')
    : '';

  if (!value) {
    throw new Error('DATABASE_URL is required for PostgreSQL connections');
  }

  if (!/^postgres(ql)?:\/\//i.test(value)) {
    throw new Error('DATABASE_URL must be a valid PostgreSQL connection string');
  }

  let parsedUrl;
  try {
    parsedUrl = new URL(value);
  } catch (error) {
    throw new Error('DATABASE_URL must be a valid PostgreSQL connection string');
  }

  if (!parsedUrl.hostname || parsedUrl.hostname === 'base') {
    throw new Error('DATABASE_URL contains an invalid PostgreSQL host');
  }

  return value;
}

const databaseUrl = getDatabaseUrl();
const pool = new Pool({
  connectionString: databaseUrl,
  ssl: { rejectUnauthorized: false },
});

function normalizeParams(params = []) {
  if (params === undefined || params === null) {
    return [];
  }

  if (Array.isArray(params)) {
    return params;
  }

  return [params];
}

function convertPlaceholders(sql) {
  let parameterIndex = 0;

  return sql.replace(/\?/g, () => {
    parameterIndex += 1;
    return `$${parameterIndex}`;
  });
}

function prepareRunSql(sql) {
  const trimmedSql = sql.trim();
  const isInsert = /^insert\s+/i.test(trimmedSql);
  const hasReturning = /\breturning\b/i.test(trimmedSql);

  if (isInsert && !hasReturning) {
    return `${trimmedSql} RETURNING id`;
  }

  return trimmedSql;
}

async function run(sql, params = []) {
  const result = await pool.query(convertPlaceholders(prepareRunSql(sql)), normalizeParams(params));

  return {
    lastID: result.rows[0]?.id != null ? Number(result.rows[0].id) : null,
    changes: result.rowCount || 0,
  };
}

async function get(sql, params = []) {
  const result = await pool.query(convertPlaceholders(sql.trim()), normalizeParams(params));
  return result.rows[0] || null;
}

async function all(sql, params = []) {
  const result = await pool.query(convertPlaceholders(sql.trim()), normalizeParams(params));
  return result.rows || [];
}

async function initDatabase() {
  await pool.query('SELECT 1');

  await run(
    `CREATE TABLE IF NOT EXISTS users (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      name TEXT NOT NULL UNIQUE,
      code TEXT NOT NULL,
      role TEXT NOT NULL,
      username TEXT,
      createdAt TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
      updatedAt TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
    )`
  );

  await run(
    `CREATE TABLE IF NOT EXISTS shops (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      name TEXT NOT NULL UNIQUE,
      code TEXT NOT NULL UNIQUE,
      phone TEXT NOT NULL,
      address TEXT NOT NULL,
      metroPrice NUMERIC(12, 2) NOT NULL,
      outsidePrice NUMERIC(12, 2) NOT NULL,
      balance NUMERIC(12, 2) NOT NULL DEFAULT 0,
      createdAt TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
      updatedAt TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
    )`
  );

  await run(
    `CREATE TABLE IF NOT EXISTS couriers (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      name TEXT NOT NULL UNIQUE,
      code TEXT NOT NULL UNIQUE,
      phone TEXT NOT NULL,
      balance NUMERIC(12, 2) NOT NULL DEFAULT 0,
      createdAt TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
      updatedAt TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
    )`
  );

  await run(
    `CREATE TABLE IF NOT EXISTS orders (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      customerName TEXT,
      customerPhone TEXT NOT NULL,
      phone TEXT NOT NULL,
      time TEXT NOT NULL,
      deliveryType TEXT NOT NULL,
      metro TEXT,
      metroName TEXT,
      address TEXT,
      price NUMERIC(12, 2) NOT NULL,
      status TEXT NOT NULL DEFAULT 'pending',
      storeId INTEGER NOT NULL,
      storeName TEXT,
      storePhone TEXT,
      storeAddress TEXT,
      courierId INTEGER,
      cancelReason TEXT,
      cancelledBy TEXT,
      isCountedInEarnings INTEGER NOT NULL DEFAULT 1,
      createdAt TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
      updatedAt TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
      FOREIGN KEY (storeId) REFERENCES shops(id) ON DELETE CASCADE,
      FOREIGN KEY (courierId) REFERENCES couriers(id) ON DELETE SET NULL
    )`
  );

  await run(
    `CREATE TABLE IF NOT EXISTS payments (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      storeId INTEGER NOT NULL,
      amount NUMERIC(12, 2) NOT NULL,
      note TEXT,
      file TEXT,
      receiptUrl TEXT,
      receiptName TEXT,
      receiptType TEXT,
      status TEXT NOT NULL DEFAULT 'pending',
      createdAt TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
      updatedAt TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
      FOREIGN KEY (storeId) REFERENCES shops(id) ON DELETE CASCADE
    )`
  );

  await run(
    `CREATE TABLE IF NOT EXISTS expenses (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      title TEXT NOT NULL,
      amount NUMERIC(12, 2) NOT NULL,
      description TEXT,
      category TEXT,
      createdAt TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
      updatedAt TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
    )`
  );

  const existingAdmin = await get(
    `SELECT id
     FROM users
     WHERE role = ?
     LIMIT 1`,
    [ADMIN_ROLE]
  );

  if (!existingAdmin) {
    const passwordHash = await bcrypt.hash(ADMIN_PASSWORD, BCRYPT_SALT_ROUNDS);

    await run(
      `INSERT INTO users (name, code, role, username, updatedAt)
       VALUES (?, ?, ?, ?, CURRENT_TIMESTAMP)`,
      [ADMIN_USERNAME, passwordHash, ADMIN_ROLE, ADMIN_USERNAME]
    );
  }
}

module.exports = {
  databaseUrl,
  run,
  get,
  all,
  initDatabase,
};
